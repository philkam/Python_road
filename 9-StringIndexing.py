#String Indexing
#Indexing means you want to access a part of the string
#It is done using [] and is zero-based.
#Indexing can be done in reverse order, where -1 corresponds to the last character in the string.
#Bigger indexes than the length of the string will throw an error.


#usage

first_string = "Kenny"
print(first_string[0]) #fetches the first element
print(first_string[-1]) #fetches the last element in string
