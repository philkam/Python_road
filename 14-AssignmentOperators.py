#Assignment Operators
"""
Assign -> =
Add and assign -> +=
Subtract and assign -> -=
Multiply and assign -> *=
Modulus and assign -> %=
Exponent and assign -> **=
Floor and assign -> //=

"""
#Usage

a = 4
a = a + 4 #is the same as a+=4
print("The result of a+=4 is ", a)

b = 4
b += 4 # is the same as b = b+4
print("The result of b = b + 4 is ", b)